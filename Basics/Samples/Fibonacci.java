
package com.mycompany.fibonacci;

public class Fibonacci {

    public static void main(String[] args) {
        int f = 0;
        int s = 1;
        int t;
        int n = 8;
        System.out.println(f);
        System.out.println(s);
        
        for(int i=3;i<=n;i++){
            t=f+s;
            System.out.println(t);
            f=s;
            s=t;
            
        }
    }
}
